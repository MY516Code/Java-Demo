
public class jh_01_赋值变量 {
	public static void main(String [] args) {
		float f1 = 98.4F;
		System.out.println("f1 = "+f1);
		float f2;
		f2 = f1;
		System.out.println("f2 = "+f2);
		int n = 55;
		System.out.println("n = "+n);
		double d1 = 55.3;
		System.out.println("d1 = "+d1);
		d1 = n;
		System.out.println("被赋值后的d1 = "+d1);
		
	}
}
